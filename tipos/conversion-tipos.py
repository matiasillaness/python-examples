# 11.2 float(): Convierte a flotante.
print(float(5))  # Resultado: 5.0
print(float("3.14"))  # Resultado: 3.14

# 11.3 str(): Convierte a cadena de texto.
print(str(123))  # Resultado: '123'
print(str(3.14))  # Resultado: '3.14'

# 11.4 bool(): Convierte a booleano.
print(bool(1))  # Resultado: True
