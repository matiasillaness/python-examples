# 8. Métodos para cadenas (str)

# 8.1 capitalize(): Convierte la primera letra de la cadena a mayúscula.
print("hola mundo".capitalize())  # 'Hola mundo'

# 8.2 lower(): Convierte todos los caracteres a minúscula.
print("HOLA".lower())  # 'hola'

# 8.3 upper(): Convierte todos los caracteres a mayúscula.
print("hola".upper())  # 'HOLA'

# 8.4 title(): Convierte la primera letra de cada palabra a mayúscula.
print("hola mundo".title())  # 'Hola Mundo'

# 8.5 swapcase(): Invierte las mayúsculas y minúsculas.
print("HoLa".swapcase())  # 'hOlA'

# 8.6 strip(): Elimina espacios en blanco (u otros caracteres) al inicio y final.
print("  hola  ".strip())  # 'hola'

# 8.7 lstrip(): Elimina espacios en blanco (u otros caracteres) al inicio.
print("  hola".lstrip())  # 'hola'

# 8.8 rstrip(): Elimina espacios en blanco (u otros caracteres) al final.
print("hola  ".rstrip())  # 'hola'
